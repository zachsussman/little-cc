#pragma once

enum token_type_e {
    NAME,
    NUMBER,

    OPEN_BRACE,
    CLOSED_BRACE,

    SEMICOLON,

    OP_PLUS,
    OP_MINUS,
    OP_MUL,
    OP_DIV,
    OP_SINGLE_AND,
    OP_SINGLE_OR,
    OP_BOOL_AND,
    OP_BOOL_OR,
    OP_ASSIGN,
    OP_EQUALS,
    OP_NOT_BANG,
    OP_NOT_TILDE,
    OP_NOT_EQUALS,

    OP_LT,
    OP_GT,
    OP_ARROW,
    OP_DOT,

    OTHER
};
typedef enum token_type_e token_type;

typedef struct token_s token;
struct token_s {
    token_type type;
    char* repr;
};

token* token_new(token_type t, char* r);
void token_delete(token* t);
char* str_token_type(token* t);

